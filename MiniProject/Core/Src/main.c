void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART1)
    {
    	if(rx_byte == '/n') {

    		if(buffer[1] == 'D'){
    			seven_segment.digits[0] = buffer[11] ;

    			 int n = seven_segment.digits[0]  * 100 ;
    			 int c = TIM8->CCR1 % 100 ;
    			 dimstep = n + c ;



    	    	  if (enableCCR1)
    	    		  TIM8->CCR1 = dimstep >=0  ? dimstep : 0;
    	    	  if (enableCCR2)
    	    		  TIM8->CCR2 = dimstep >=0  ? dimstep : 0;
    	    	  if (enableCCR3)
    	    		  TIM8->CCR3 = dimstep >=0  ? dimstep : 0;
    	    	  if (enableCCR4)
    			  	  TIM8->CCR4 = dimstep >=0  ? dimstep : 0;


    		}else if (buffer[1] == 'L'){
    			seven_segment.digits[1] =buffer[11] ;


    			if (seven_segment.digits[1] == 0){
    						setTIM8CCR1(0); enableCCR1 = 0;
    						setTIM8CCR2(0); enableCCR2 = 0;
    						setTIM8CCR3(0); enableCCR3 = 0;
    						setTIM8CCR4(0); enableCCR4 = 0;
    					} else if (seven_segment.digits[1] == 1){
    						setTIM8CCR1(seven_segment.digits[0]); enableCCR1 = 1;
    						setTIM8CCR2(0); enableCCR2 = 0;
    						setTIM8CCR3(0); enableCCR3 = 0;
    						setTIM8CCR4(0); enableCCR4 = 0;
    					} else if (seven_segment.digits[1] == 2){
    						setTIM8CCR1(seven_segment.digits[0]); enableCCR1 = 1;
    						setTIM8CCR2(0); enableCCR2 = 0;
    						setTIM8CCR3(0); enableCCR3 = 0;
    						setTIM8CCR4(seven_segment.digits[0]); enableCCR4 = 1;
    					} else if (seven_segment.digits[1] == 3){
    						setTIM8CCR1(seven_segment.digits[0]); enableCCR1 = 1;
    						setTIM8CCR2(seven_segment.digits[0]); enableCCR2 = 1;
    						setTIM8CCR3(0); enableCCR3 = 0;
    						setTIM8CCR4(seven_segment.digits[0]); enableCCR4 = 1;
    					} else if (seven_segment.digits[1] >= 4) {
    						setTIM8CCR1(seven_segment.digits[0]); enableCCR1 = 1;
    						setTIM8CCR2(seven_segment.digits[0]); enableCCR2 = 1;
    						setTIM8CCR3(seven_segment.digits[0]); enableCCR3 = 1;
    						setTIM8CCR4(seven_segment.digits[0]); enableCCR4 = 1;
    					}

    		}else {
    			seven_segment.digits[2] = buffer[11] ;
    		}

    		uart_index = 0;
    	}

    	buffer[uart_index++] = rx_byte ;


    	HAL_UARTEx_ReceiveToIdle_IT(&huart1, &rx_byte, 1);
    }


}

